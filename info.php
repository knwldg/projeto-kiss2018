    <h1>
        Como Jogar?
    </h1>
    <p class="info_cubo">
Completa Desafios para obteres chaves !
        Com as chaves podes abrir baús, que contêm cromos que podes colecionar e trocar por prémios!
        Os baús podem ser comuns (baús azuis) e dão te x cromos, mas há sempre a possibilidade de ser um baú raro (baú dourado)que te dá mais e melhores cromos!
        Os cromos também têm grau de raridade, sendo que podes ficar com alguns repetidos, que te permite evoluir o cromo dourado (mais raro)
        Para reclamares o teu prémio, vai á pagina dos prémios e segue o que a descrição do prémio te diz!
